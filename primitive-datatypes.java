public class PrimitiveDatatypes{
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        float f=24.32f;
        System.out.println(f);
        char c='A';
        System.out.println(c);
        double d=34.735643;
        System.out.println(d);
        long l=341279463L;
        System.out.println(l);
        boolean b=true;
        System.out.println(b);
    }
}
